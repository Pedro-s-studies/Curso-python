# Inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Real = ", numero_real)

# type()
print("Tipo da variável inteiro = ", type(numero_inteiro))
print("Tipo da variável real = ", type(numero_real))

# Fun. Aritméticas
# Soma
soma = 1 + 1
print("1 + 1 = ", soma)

# Subtracao
subtracao = 1 - 1
print("1 - 1 = ", subtracao)

# Multiplicacao
multiplicacao = 2 * 2
print("2 x 2 = ", multiplicacao)

# Divisao com ponto flutuante
divisao = 5 / 2
print("5 / 2 = ", divisao)

# Divisao retornando um numero inteiro
divisao_int = 5 // 2
print("5 / 2 = ", divisao_int)

# Modulo
modulo = 5 % 2
print("5 % 2 = ", modulo)

# Transformar o valor para inteiro
int(divisao)

# Transformar o valor para float
float(soma)
